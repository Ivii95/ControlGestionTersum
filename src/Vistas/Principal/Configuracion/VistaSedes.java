    private void tabla_sedesMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tabla_sedesMouseClicked
        if (isAlreadyOneClick) {
            sede = repoSedes.getSedeById(UtilidadesPantalla.getIdSelected(tabla_sedes));//Cogemos el trabajador seleccionado.
            if (sede != null) {
                txtCodigo.setText(sede.getCodigo() + "");
                txtCiudad.setText(sede.getCiudad());
            }
            isAlreadyOneClick = false;
        } else {
            isAlreadyOneClick = true;
            Timer t = new Timer("doubleclickTimer", false);
            t.schedule(new TimerTask() {
                @Override
                public void run() {
                    isAlreadyOneClick = false;
                }
            }, 250);
        }
    }//GEN-LAST:event_tabla_sedesMouseClicked

