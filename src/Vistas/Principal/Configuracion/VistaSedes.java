    private void btn_borrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_borrarActionPerformed
        if (tabla_sedes.getSelectedRow() != -1) {
            int filaSeleccionada = tabla_sedes.getSelectedRow();
            int opcionborrar = JOptionPane.showConfirmDialog(rootPane, "¿Estas seguro de borrar el registro?\n"
                    + "Sede: " + tabla_sedes.getValueAt(filaSeleccionada, 1) + " " + (String) tabla_sedes.getValueAt(filaSeleccionada, 2));
            switch (opcionborrar) {
                case 0://OPCION SI BORRAR TRABAJADOR
                    int id = UtilidadesPantalla.getIdSelected(tabla_sedes);
                    if (repoSedes.deleteSede(id)) {
                        restablecerTabla();
                    }
                    break;
            }
        } else {
            JOptionPane.showMessageDialog(this, "Debes seleccionar una sede");
        }
    }//GEN-LAST:event_btn_borrarActionPerformed

    private void btn_modificarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_modificarActionPerformed
        if (tabla_sedes.getSelectedRow() != -1) {
            sede = repoSedes.getSedeById(UtilidadesPantalla.getIdSelected(tabla_sedes));//Cogemos el trabajador seleccionado.
            if (sede != null) {
                sede.setCodigo(Integer.parseInt(txtCodigo.getText()));
                sede.setCiudad(txtCiudad.getText());
            }
            Sede get = sede;
            if (repoSedes.update(get)) {
                restablecerTabla();
            }
        } else {
            JOptionPane.showMessageDialog(this, "Debes seleccionar un trabajador");
        }
    }//GEN-LAST:event_btn_modificarActionPerformed

    private void btn_añadirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_añadirActionPerformed
        int codigo = Integer.parseInt(txtCodigo.getText());
        String ciudad = txtCiudad.getText();
        Sede get = new Sede(0, codigo, ciudad);

        if (repoSedes.insert(get)) {
            restablecerTabla();
        }
    }//GEN-LAST:event_btn_añadirActionPerformed

