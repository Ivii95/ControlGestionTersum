    private void comprobarNumero(KeyEvent evt) {

        if (Character.isDigit(evt.getKeyChar()) == false) {
            //JOptionPane.showMessageDialog(rootPane, "No puedes introducir letras");
            evt.consume();
        }
    }

    private void comprobarCodigo(KeyEvent evt) {
        try {
            if (repoUsuario.ifIdExist(Integer.parseInt(txt_id.getText())) && lbl_id.getForeground().equals(new java.awt.Color(204, 204, 204))) {
                JOptionPane.showMessageDialog(this, "Codigo usado", "Codigo", JOptionPane.WARNING_MESSAGE);
                lbl_id.setForeground(Color.red);
            } else if (!repoUsuario.ifIdExist(Integer.parseInt(txt_id.getText())) && lbl_id.getForeground().equals(Color.red)) {
                lbl_id.setForeground(new java.awt.Color(204, 204, 204));
            }
        } catch (NumberFormatException e) {

        }
    }
