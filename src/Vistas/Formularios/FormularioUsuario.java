    private void AceptarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_AceptarActionPerformed
        boolean correcto = false;
        try {
            if (!comboRol.getSelectedItem().equals("Seleccione uno")) {//Rol
                if (!txt_id.getText().equals("") && txt_id.getText() != null) {//Id
                    if (!repoUsuario.ifIdExist(Integer.parseInt(txt_id.getText()))) {//Id repetido
                        usuarioModificar.setId(Integer.parseInt(txt_id.getText()));
                        if (txt_nombre.getText() != null && !txt_nombre.getText().equals("")) {//Nombre
                            usuarioModificar.setNombre(txt_nombre.getText());
                            if (txt_pass.getText() != null && !txt_pass.getText().equals("")) {//Pass
                                usuarioModificar.setPassword(Utilidades.Utilidades.encriptaEnMD5(txt_pass.getText()));
                                correcto = true;
                            } else {
                                JOptionPane.showMessageDialog(this, "Necesita ingresar una contrase√±a", "CONTRASE√ëA", JOptionPane.INFORMATION_MESSAGE);
                            }
                        } else {
                            JOptionPane.showMessageDialog(this, "Necesita ingresar un nombre", "NOMBRE", JOptionPane.INFORMATION_MESSAGE);
                        }
                    } else {
                        JOptionPane.showMessageDialog(this, "El id ya existe en la base de datos", "ID", JOptionPane.WARNING_MESSAGE);
                    }
                } else {
                    JOptionPane.showMessageDialog(this, "Necesita ingresar un id", "ID", JOptionPane.INFORMATION_MESSAGE);
                }
            } else {
                JOptionPane.showMessageDialog(this, "Necesita ingresar un Rol", "ROL", JOptionPane.INFORMATION_MESSAGE);
            }
            if (correcto) {
                this.setVisible(false);
            }
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "No se pueden meter n√∫meros en algunos campos", "Errores con los n√∫meros", JOptionPane.WARNING_MESSAGE);
            System.out.println(e.getMessage());
        } catch (HeadlessException e) {
            JOptionPane.showMessageDialog(this, "Excepcion al meter los datos del formulario", "Error", JOptionPane.ERROR_MESSAGE);
            System.out.println(e.getMessage());
        }
    }//GEN-LAST:event_AceptarActionPerformed

